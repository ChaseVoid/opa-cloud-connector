package com.ajay.opa.cloud.metadata;

/**
 * This class represents the enumeration types available to the WSDL defined 
 * GetMetadataResponse Field, and is used as part of an abstract layer representing 
 * part of the 'camera_company' database's structure.
 */
public enum FieldType {
	STRING, 
	DATETIME, 
	INTEGER, 
	BOOLEAN, 
	DATE, 
	DECIMAL, 
	LONG, 
	TIMEOFDAY
}
